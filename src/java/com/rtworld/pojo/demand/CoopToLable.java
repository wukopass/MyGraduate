package com.rtworld.pojo.demand;

public class CoopToLable {
    private int cid;
    private int lid;

    public CoopToLable() {
    }

    public CoopToLable(int cid, int lid) {
        this.cid = cid;
        this.lid = lid;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public int getLid() {
        return lid;
    }

    public void setLid(int lid) {
        this.lid = lid;
    }
}
